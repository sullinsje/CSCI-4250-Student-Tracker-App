public struct RoleColors
{
    public string Primary { get; set; }        // Base color (e.g., button background)
    public string Hover { get; set; }          // Hover color
    public string ShadowRgb { get; set; }      // RGB for box-shadows/focus effects
    public string GradientStart { get; set; }  // Gradient start color
    public string GradientEnd { get; set; }    // Gradient end color
}